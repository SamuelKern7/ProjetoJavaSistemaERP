    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
       
        if (TxtEmail.getText().trim().length() > 0 && String.valueOf(txtSenha.getPassword()).trim().length() > 0) {

            Funcionarios o = new Funcionarios();
            o.setEmail(TxtEmail.getText());
            o.setSenha(String.valueOf(txtSenha.getPassword()));

            if (new FuncionariosDAO().autenticar(o)) {
                new FrmPrincipal().setVisible(true);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usu치rio ou senha incorretos!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Preencha usu치rio e senha adequadamente.");

            TxtEmail.requestFocus();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

    
