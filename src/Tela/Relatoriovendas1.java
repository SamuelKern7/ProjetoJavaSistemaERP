    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.dispose();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate data_inicio = LocalDate.parse(tffDataInicio.getText(), formato);
        LocalDate data_fim = LocalDate.parse(tffDataFinal.getText(), formato);
        String data_inicios = data_inicio.format(formato);
        String data_fims = data_fim.format(formato);
        
        try {
            // Compila o relatorio
            JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("/Relatorios/Relatorio_Vendas.jrxml"));
            
            Map parametros = new HashMap();
            parametros.put("dataind", data_inicios);
            parametros.put("datafin", data_fims);
            System.out.println("s" + parametros +  "s" + "s" + parametros);
            
// Executa relatoio
            JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ConexaoBD.getInstance().getConnection());

            // Exibe resultado em video
            JasperViewer.viewReport(impressao, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gerar relat贸rio: " + e);
        }

    }//GEN-LAST:event_jButton2ActionPerformed


